package com.example.rikuwaapp.Entidad;

public class Personal {

    String unidadNombre;
    String nombreCompletotmp;
    String nombreCompleto;
    String dni;
    String domicilio;
    String celular;
    String cargo;

    public String getNombreCompletotmp() {
        return nombreCompletotmp;
    }

    public void setNombreCompletotmp(String nombreCompletotmp) {
        this.nombreCompletotmp = nombreCompletotmp;
    }

    public String getUnidadNombre() {
        return unidadNombre;
    }

    public void setUnidadNombre(String unidadNombre) {
        this.unidadNombre = unidadNombre;
    }

    public String getNombreCompleto() {
        return nombreCompleto;
    }

    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getDomicilio() {
        return domicilio;
    }

    public void setDomicilio(String domicilio) {
        this.domicilio = domicilio;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }
}
